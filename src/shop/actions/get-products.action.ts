import {tesloApi} from "@/api/tesloApi.ts";
import type ProductsResponse from "@/interfaces/products.response.ts";

interface Options{
    limit?: number | string;
    offset?: number | string;
    gender?: string;
    sizes?: string;
    minPrice?: number;
    maxPrice?: number;
    q?: string;
}

export const getProductsAction = async(options:Options):Promise<ProductsResponse> => {
    const {limit, offset, gender, sizes, minPrice, maxPrice, q} = options;
    const {data} = await tesloApi.get<ProductsResponse>('/products', {
        params: {
            limit, offset, gender, sizes, minPrice, maxPrice, q
        }
    });

    const productsWithImageURL = data.products.map((producto) => ({
        ...producto,
        images: producto.images.map((image) => `${import.meta.env.VITE_API_URL}/files/product/${image}`)
    }))
    return {...data, products: productsWithImageURL};
};